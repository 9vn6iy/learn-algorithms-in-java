package org.vny.leetcode.problems.p0148;

class ListNode {
  int val;
  ListNode next;

  ListNode() {
  }

  ListNode(int val) {
    this.val = val;
  }

  ListNode(int val, ListNode next) {
    this.val = val;
    this.next = next;
  }
}

public interface Main {

  ListNode sortList(ListNode head);
}
